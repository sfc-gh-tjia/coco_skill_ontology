USE SCHEMA TEMP.ONTOLOGY_POC;

-- VW_ONT_SUBCLASS_OF: Resolved subClassOf relationships
CREATE OR REPLACE VIEW VW_ONT_SUBCLASS_OF AS
SELECT
    e.SRC_ID AS CHILD_ID,
    e.DST_ID AS PARENT_ID,
    'subClassOf' AS REL_TYPE,
    child.NAME AS CHILD_NAME,
    parent.NAME AS PARENT_NAME,
    child.NODE_TYPE AS CHILD_TYPE,
    parent.NODE_TYPE AS PARENT_TYPE
FROM TEMP.ONTOLOGY_POC.KG_EDGE e
LEFT JOIN TEMP.ONTOLOGY_POC.KG_NODE child ON e.SRC_ID = child.NODE_ID
LEFT JOIN TEMP.ONTOLOGY_POC.KG_NODE parent ON e.DST_ID = parent.NODE_ID
WHERE e.EDGE_TYPE = 'subClassOf'
;

