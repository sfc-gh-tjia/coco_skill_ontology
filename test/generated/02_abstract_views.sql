-- VW_ONT_HIERARCHY_STATS: Hierarchy statistics
CREATE OR REPLACE VIEW VW_ONT_HIERARCHY_STATS AS
SELECT
    e.NODE_ID AS NODE_ID,
    e.NAME AS NODE_NAME,
    COUNT(DISTINCT child.CHILD_ID) AS DIRECT_CHILDREN_COUNT,
    COUNT(DISTINCT parent.PARENT_ID) AS DIRECT_PARENTS_COUNT
FROM TEMP.ONTOLOGY_POC.KG_NODE e
LEFT JOIN VW_ONT_SUBCLASS_OF child ON e.NODE_ID = child.PARENT_ID
LEFT JOIN VW_ONT_SUBCLASS_OF parent ON e.NODE_ID = parent.CHILD_ID
WHERE NODE_TYPE IN ('CellType', 'cl')
GROUP BY e.NODE_ID, e.NAME;
